/**
 * Searches products across all searchable fields:
 * - name
 * - category
 * - subCategory
 * - tags
 * - colors (color names)
 * - sizes
 * - useCase
 * 
 * Case-insensitive matching.
 * Does NOT inspect file names or query DOM.
 * Does NOT re-index on every render.
 */
export const searchProducts = (query: string): Product[] => {
  if (!query || !query.trim()) {
    return [];
  }

  const searchTerms = query.toLowerCase().trim().split(/\s+/);
  
  return products.filter(product => {
    // Build searchable text from product fields
    const searchableText = [
      product.name,
      product.category,
      product.subCategory || '',
      ...product.tags,
      ...product.colors.map(c => c.name),
      ...product.sizes,
      ...(product.useCase || []),
      product.gender || '',
    ].join(' ').toLowerCase();

    // Check if all search terms are present
    return searchTerms.every(term => searchableText.includes(term));
  });
};

/**
 * Advanced search with filters applied
 * Combines text search with category, price, size, and color filters
 */
export const searchProductsWithFilters = (
  query: string,
  filters?: SearchFilters
): Product[] => {
  let results = searchProducts(query);

  if (filters) {
    // Apply category filter
    if (filters.category) {
      results = results.filter(p => p.category === filters.category);
    }

    // Apply price range filter
    if (filters.priceRange) {
      const [min, max] = filters.priceRange;
      results = results.filter(p => p.price >= min && p.price <= max);
    }

    // Apply size filter
    if (filters.sizes && filters.sizes.length > 0) {
      results = results.filter(p => 
        p.sizes.some(size => filters.sizes!.includes(size))
      );
    }

    // Apply color filter
    if (filters.colors && filters.colors.length > 0) {
      results = results.filter(p =>
        p.colors.some(color => filters.colors!.includes(color.name))
      );
    }

    // Apply gender filter
    if (filters.gender) {
      results = results.filter(p => p.gender === filters.gender);
    }

    // Apply useCase filter
    if (filters.useCase && filters.useCase.length > 0) {
      results = results.filter(p =>
        p.useCase?.some(uc => filters.useCase!.includes(uc))
      );
    }

    // Apply sorting
    if (filters.sortBy) {
      switch (filters.sortBy) {
        case 'price-asc':
          results.sort((a, b) => a.price - b.price);
          break;
        case 'price-desc':
          results.sort((a, b) => b.price - a.price);
          break;
        case 'newest':
          results.sort((a, b) => (b.isNew ? 1 : 0) - (a.isNew ? 1 : 0));
          break;
        case 'popular':
          results.sort((a, b) => b.reviewCount - a.reviewCount);
          break;
        case 'rating':
          results.sort((a, b) => b.rating - a.rating);
          break;
      }
    }
  }

  return results;
};

